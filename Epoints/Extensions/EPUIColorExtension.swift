import UIKit
extension UIColor {
    
    static func yellowThemeColor() -> UIColor
    {
        return UIColor(red: 255.0/255.0, green: 200.0/255.0, blue: 71.0/255.0, alpha: 1.0) //HEX Value: #FFC847
    }
    
}
